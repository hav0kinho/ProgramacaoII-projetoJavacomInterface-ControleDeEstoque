    private void jButtonEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEntrarActionPerformed
        //Verifica se o usuario pode logar ou não, caso sim ele fechará a janela e abrirá o estoque, senão ele exibira uma mensagem falando que não existe esse usuário
        String usuario = jTextFieldUsuario.getText();
        String senha = jPasswordFieldSenha.getText();
         
        //Caso tenha sido verificado, o programa irá abrir a janela do Estoque
        if(fachada.verificarUsuario(usuario, senha)){
            EstoqueView estoqueView = new EstoqueView(fachada);
            estoqueView.setVisible(true);
            this.setVisible(false);
        } else {
            //Caso não tenha sido encontrado, o programa irá mostrar uma mensagem
            JOptionPane.showMessageDialog(rootPane, "Senha ou Usuário incorretos");
        }
    }//GEN-LAST:event_jButtonEntrarActionPerformed

